// max number find
function isLarge(one,two,three){
    let result = Math.max(one,two,three)
    console.log('large number is',result)
}

isLarge(1250,350,450)

// min number

function isLower(one,two,three){
    let result = Math.min(one,two,three)
    console.log('lower number is',result)
}

isLower(1250,350,450)